#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - Entry point
 *
 *
 * Description : print the last digit of the number stored in the variable n.
 *
 * Return: 0 (success)
*/
int main(void)
{
char ch = 'a';
while (ch <= 'z');
{
putchar(ch);
ch++;
}
putchar('\n');
return (0);
}

